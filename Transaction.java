public class Transaction {
	private String name;
	private double calories;
	private int price;
	private int cashIn;
	private int change;
	
	public Transaction(String name, double calories, int price, int cashIn, int change) {
		this.name = name;
		this.calories = calories;
		this.price = price;
		this.cashIn = cashIn;
		this.change = change;
	}
	
	/*getters and setters*/
}